// Ejercicio 87: Uso de la función charCodeAt para obtener el valor numérico de un carácter.

let cadena = 'Ajax';

console.log(cadena.charCodeAt(0));
console.log(cadena.charCodeAt(1));

cadena = '\u0041';
console.log(cadena.charCodeAt(0));

cadena = '\n';
console.log(cadena.charCodeAt(0));

cadena = '\t';
console.log(cadena.charCodeAt(0));
